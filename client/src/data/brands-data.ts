export function getBrandBySlug(slug: string): BrandEntry | undefined {
  return BRANDS.find(b => b.slug === slug);
}

export function getBrandsByIndustry(industry: string): BrandEntry[] {
  if (industry === "All") return BRANDS;
  return BRANDS.filter(b => b.industry === industry);
}

